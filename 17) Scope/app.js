// Scope 
// Curly Braces {} Har Programing Language main Scope hain
// Object main curly  Braces {} Objects Declaration hai
// Function , array and conditions ke sath curly  Braces {} scope hain


let a = 10;
const b = 20;
var c = 30;
if (true){
    let a = 10  // Block Scope
    const b = 20 // Block Scope
    var c = 30 // Global Scope
}
console.log(a);
console.log(b);
console.log(c);


